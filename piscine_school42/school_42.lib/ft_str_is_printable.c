int	ft_str_is_printable(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= (char)32 && str[i] <= (char)127)
		{
			i += 1;
			continue;
		}
		return (0);
	}
	return (1);
}
